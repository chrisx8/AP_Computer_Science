//Prints a StairCase

public class StairCase
{
    public static void main(String[] args) {
        System.out.println("\t\t\t+---+\n\t\t\t|   |");
        System.out.println("\t\t+---+---+\n\t\t|   |   |");
        System.out.println("\t+---+---+---+\n\t|   |   |   |");
        System.out.println("+---+---+---+---+\n|   |   |   |   |");
        System.out.println("+---+---+---+---+");
    }
}